package test_table;

import field.TableField;
import field.DataType;
import table.Table;

/**
 * Created by said on 02.05.17.
 */
@Table(name = "foo1")
public class Foo1 {
    @TableField(id = true, autoGeneratedId = true, dataType = DataType.LONG, fieldName = "id")
    private long id;

    @TableField(dataType = DataType.STRING, fieldName = "test_name")
    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Foo1{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
