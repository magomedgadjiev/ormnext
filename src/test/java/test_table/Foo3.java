package test_table;

import field.TableField;
import field.DataType;
import field.ManyToMany;
import table.Table;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by said on 06.05.17.
 */

@Table(name = "foo3")
public class Foo3 {
    @TableField(id = true, autoGeneratedId = true, dataType = DataType.LONG, fieldName = "id")
    private long id;

    @TableField(dataType = DataType.STRING, fieldName = "test_name")
    private String name;

    @ManyToMany(mappedBy = "foo3List")
    List<Foo> fooList = new ArrayList<>();

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<Foo> getFooList() {
        return fooList;
    }

    @Override
    public String toString() {
        return "test_table.Foo2{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
